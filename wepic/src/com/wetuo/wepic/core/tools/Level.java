public class Level {
	public static int[] levelforUser(int totalMark){
		int star=totalMark/200;
		int  jewel=0;
		if(star>5){
		if(star%5==0){
			jewel=star%5;
			star=0;
		}else{
			jewel=star/5;
			star=star%5;
		}
		}
		int[] is={jewel,star};
		
		
		
		return is;
		
		
		
		
	}





}
