function searchonfocus(){
	var search=document.getElementById("inputkeyword");
	if(search.value == "搜索"){
		 search.value='';
		 search.style.color='#959595'
		}
	}
function searchonblur(){
	var search=document.getElementById("inputkeyword");
	if(search.value == ""){
		 search.value='搜索';
		 search.style.color='#959595'
		}
	}



